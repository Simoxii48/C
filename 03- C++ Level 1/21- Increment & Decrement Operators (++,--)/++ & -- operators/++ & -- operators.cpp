
#include <iostream>
using namespace std;


int main()
{
    int A = 10, B = 20;

    A++;
    B--;

    cout << "A = " << A << endl;
    cout << "B = " << B << endl;

    // it's a difference between the 2 ways will know in the next lesson
    ++A;
    --B;

    cout << "A = " << A << endl;
    cout << "B = " << B << endl;
}