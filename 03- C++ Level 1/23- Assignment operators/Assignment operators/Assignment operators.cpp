
#include <iostream>
using namespace std;

int main()
{
    int A = 10, B = 20;

    A += B; // Like we write A = A + B
    cout << "A = " << A << endl;

    A -= B; // Like we write A = A - B
    cout << "A = " << A << endl;

    A *= B; // Like we write A = A * B
    cout << "A = " << A << endl;

    A /= B; // Like we write A = A / B
    cout << "A = " << A << endl;

    A %= B; // Like we write A = A % B
    cout << "A = " << A << endl;
}